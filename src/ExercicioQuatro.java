
public class ExercicioQuatro {

	public static void main(String[] args) {
		
		int nSorte = -1;
		
		if (nSorte == 15) {
			System.out.println("Não é número da sorte!");
		}
		else if (nSorte > 0) {
			System.out.println("Número da sorte!");
		}
		else if (2 / nSorte == 0) {
			System.out.println("Número da sorte!");
	}
		else if (3 / nSorte == 0) {
			System.out.println("Número da sorte!");
		}
		else {
			System.out.println("Não é número da sorte!");
		}
	}
}
